//-----------Number to string-------
let num = 30;
let stringNum = String(num);
console.log(stringNum); //30
console.log(typeof stringNum); // string

// --------Boolean to string conversion------
let myBool = false;
let myBoolString = String(myBool);
console.log(myBoolString); // false
console.log(typeof myBoolString); // string

// --------Undefined to string conversion ----
let myUndefined = undefined;
let myUndefinedString = String(myUndefined);
console.log(myUndefinedString); // Undefined
console.log(typeof myUndefinedString); // string

// ----------null to string conversion-------
let myNull = null;
let myNullString = String(myNull);
console.log(myNullString); // null
console.log(typeof myNullString); // string
